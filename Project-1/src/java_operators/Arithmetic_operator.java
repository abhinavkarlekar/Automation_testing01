package java_operators;

public class Arithmetic_operator {

// 1) Arithmetic operators           // This operators works only with numeric types of data.(int,float,double,)
	
// (+) Addition operator.
// (-) Subtraction operator.
// (*) Multiplication operator.
// (/) Division operator.
// (%) Modulo division operator. 
	
	public static void main(String[] args) {
		
	int a=20, b=10;

	System.out.println("The sum of a and b is : "+ (a+b));                  // The (+) symbol use to perform addition of two numbers. 
	System.out.println("The subtraction of a and b is : "+ (a-b));         // The (-) symbol use to perform subtraction of two numbers. 
	System.out.println("The multiplication of a and b is : "+ (a*b));     // The (*) symbol use to perform multiplication of two numbers. 
	System.out.println("The division of a and b is : "+ (a/b));          // The (/) symbol use to perform division of two numbers. (/) operator return the quotient value. 
	System.out.println("The modulo division of a and b is : "+ (a%b));  // The (%) symbol use to perform modulo division of two numbers. (%) operator return the reminder value.




	}

}

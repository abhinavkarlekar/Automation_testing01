package java_operators;

public class Assignment_operator {
	
// Assignment operators use to assign values to variables. 

// = , += , -= , *= , /= , %=  those are assignment operators. 

	public static void main(String[] args) {
		
		int a = 100;
		
		a=a+20;         // we use assignment (+=) operator to increase the variable value more than 1. 
		
		//a+=30;         // we can also write this way in this we don t have to write variable multiple times.
		
		System.out.println(a); 
		
		
		int b = 50;
		
		b=b-10;             // decrease the value more than 1. 
		
		System.out.println(b);
	}

}

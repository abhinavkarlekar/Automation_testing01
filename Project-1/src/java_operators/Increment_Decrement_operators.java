package java_operators;

public class Increment_Decrement_operators {

	// ++ is called increment operator. use to increase value by 1. 
   // -- is called decrement operator.  use to decrease value by 1. 
	
	public static void main(String[] args) {
		
		int a = 10;
		System.out.println(a);
		
		//a = a+1;                 // This is normal way we can increment a value for a variable. 
		
		a++;                      // this is Using increment operator. 
		System.out.println(a);
		
		
		int b = 20;
		System.out.println(b);
		
		b--; 
		System.out.println(b); 
	}

}

package java_Loop_statements;

public class While_loop_statement {

// if you are uncertain about the number of iterations and the loop termination is based on condition, the while loop is good choice. 
	
	public static void main(String[] args) {
		
	int i = 1;                    // in while loop 1st we have to initialize the variable and its value. 
	
	while (i<=10) {                   // inside the while loop we have to put condition. 
		
	System.out.println(i);
	
	i++;                              // then we have to put increment with print statement. 
	
	
	}
	}

}

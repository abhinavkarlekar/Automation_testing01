package java_Loop_statements;

public class For_loop_statement {
// if you know the no of iterations in advance, the for loop is often the most suitable. 
// a set of statements which we are going to execute multiple times or repetitively it is called as loop. 
// Loop execute block of code as long as specified condition is true. 
// in for loop we have to specifies initialization of variable (starting point of loop). then condition and increment/decrement. inside for loop. 
	
	public static void main(String[] args) {
		
// Condition : print the numbers 1 to 10. 
		
	for (int i=1; i<=10; i++)         // in for loop we have to specifies initialization of variable (starting point of loop). 
		                              // condition and increment/decrement inside for loop. 
	{
	
		System.out.println(i); 
	
}
	}

}
